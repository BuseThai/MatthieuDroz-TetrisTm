class Tetrimino:
    def __init__(self,x,y):
        self.x = x
        self.y = y
        self.fig = self.rndfig()
        self.rot = 0
    
    def rndfig(self):
        return figtype[random.randint(0,len(figtype)-1)]

    def getFig(self):
        return self.fig[self.rot]
    
    def rotate(self):
        if self.rot == 3:
            self.rot=0
        else:
            self.rot=self.rot+1
        

    

